#include <stdio.h>

int main() {
    int age = 25;
    char grade = "A";
    float pi = 3.14159;
    double avogadro = 6.02214076e23;

    printf("Age: %d\n", age);
    printf("Grade: %c\n", grade);
    printf("Pi: %f\n", pi);
    printf("Avogadro's number: %lf\n", avogadro);

    return 0;
}